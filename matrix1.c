#include<stdio.h>
int main()
{
 int a,s,c,f;

 printf("\n Enter A Number: ");
 scanf("%d",&a);

 s=a*a;  //Square = number * number
  printf("Square of %d is = %d\n",a,s);
 c=a*a*a;  //Cube = Square * number
  printf("\n\n Cube of %d is = %d\n",a,c);
 f=a*a*a*a; //fourth power=cube*number
 printf("fourth power of %d is=%d\n",a,f);





 return 0;

}

